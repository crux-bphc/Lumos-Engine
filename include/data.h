#pragma once

/**
 * @file data.h
 * @brief External declarations for window dimensions in the Lumos engine.
 */

/**
 * @brief External variable representing the height of the application window.
 *
 * The WINDOW_HEIGHT variable stores the height of the application window in pixels.
 * It is declared as an external variable to be shared across different source files.
 */
extern int WINDOW_HEIGHT;

/**
 * @brief External variable representing the width of the application window.
 *
 * The WINDOW_WIDTH variable stores the width of the application window in pixels.
 * It is declared as an external variable to be shared across different source files.
 */
extern int WINDOW_WIDTH;
